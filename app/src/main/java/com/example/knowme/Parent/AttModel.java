package com.example.knowme.Parent;

public class AttModel {
    String date,status;

    public AttModel() {
    }

    public AttModel(String date, String status) {
        this.date = date;
        this.status = status;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
